###     Lambdas     ###

#Las lambdas nos sirven para hacer funciones sin tener que nombrarlas y asignandolas a una variable, son mas simplificadas que las funciones y nos puede servir para casos concretos. 
sum_two_values = lambda firts_value, second_value: firts_value + second_value
print(sum_two_values(2, 8))

multi_two_values = lambda firts_value, second_value: firts_value * second_value - 5
print(multi_two_values(3, 4))